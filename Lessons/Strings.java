import java.util.*;

public class Strings {
    public static void main(String args[]) {

        // concatenation
        String firstname = "ojas";
        String lastname = "patil";
        String fullname = firstname + " " + lastname;
        System.out.println(fullname.length()); // length -> gives the spaces number
        // Scanner sc = new Scanner(System.in);
        // String names = sc.nextLine(); // next -> gives name only
        // nextLine -> gives full name
        // System.out.println("The name is :" + names);

        // charAt -> prints every letters
        for (int i = 0; i < fullname.length(); i++) {
            System.out.println(fullname.charAt(i));
        }

    }
}
